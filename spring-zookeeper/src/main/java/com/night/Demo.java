package com.night;

public class Demo {
    public static void main(String[] args) {
        System.out.println(1 & 1);
        System.out.println(1 & 0);
        System.out.println(0 & 0);
        System.out.println(1 | 0);
        System.out.println(1 | 1);
        System.out.println(0 | 0);
        System.out.println(1 ^ 1);
        System.out.println(1 ^ 0);
        System.out.println(0 ^ 0);
    }
}
